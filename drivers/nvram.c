#include <osconfig.h>
#include <drivers/nvram.h>
#include <types.h>
#include <bits.h>

// will return in Kilobytes
u32_t nvram_get_mem_info(u32_t name)
{
  u32_t ret = (nvram_read(name) * 1024);
  return ROUND_DOWN(ret ,PG_SIZE);
}
      
